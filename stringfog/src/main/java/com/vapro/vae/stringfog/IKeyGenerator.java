package com.vapro.vae.stringfog;

/**
 * ===============================================================================
 * 功能：每串密钥生成器契约（构建期为「每个字符串独立密钥」策略提供密钥字面量）。
 * 函数简介：构建期插桩每遇到一个待加密字符串字面量，调用本接口生成该串专属密钥；
 *   密钥以 String 形态返回，随密文一并写入字节码常量池，运行时按同一密钥解密。
 *
 * 设计动机（诚实说明其安全边界）：
 *   密钥随插桩字节码嵌入 APK（原版 StringFog 亦然），故「每串随机密钥」并不提供密码学机密性——
 *   密钥就在密文旁边。它的真实价值是**抗统计/模式分析**：
 *     1. 相同明文（如同一 URL 在多处出现）加密后密文各不相同，无法据密文相等性聚类；
 *     2. 全 APK 不存在可 grep 的「共享密钥」常量，抬高批量脱壳/自动化还原成本。
 *
 * 契约约束：
 *   1. 返回的密钥字符串必须能经 UTF-8 稳定往返（构建期 keyLiteral.getBytes(UTF_8) 与运行时一致），
 *      故内置 {@link RandomKeyGenerator} 只产出可打印 ASCII 字母数字，规避代理对/非法码点问题。
 *   2. 实现须线程安全——构建期可能多线程并发插桩。
 * ===============================================================================
 */
public interface IKeyGenerator {

    /**
     * 为一个待加密字符串生成其专属密钥。
     *
     * @param value 待加密的原始明文字面量（供需要「按内容派生密钥」的自定义实现使用；
     *              内置随机实现忽略之，每次产出独立随机密钥）
     * @return 该串专属密钥字符串（其 UTF-8 字节即算法密钥），非 null 且非空
     */
    String generateKey(String value);
}
